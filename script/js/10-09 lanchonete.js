// Aplicação para lanchonete

var mesa = "3";

switch (mesa) {
    case "1":
        console.log("Mesa com espaço para 2 pessoas");
        break;

    case "2":
        console.log("Mesa com espaço para 4 pessoas")
        break;

    case "3":
        console.log("Mesa com espaço para 6 pessoas");
        break;
    
    case "4":
        console.log("Mesa com espaço para 8 pessoas");
        break;
    
    default:
        console.log("Consultar disponibilidade no local")
}
